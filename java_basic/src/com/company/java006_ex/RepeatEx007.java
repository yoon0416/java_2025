package com.company.java006_ex;

public class RepeatEx007 {

	public static void main(String[] args) {
		System.out.println("1번 문제");
		//for문
		for(int i=1; i<=5; i++) {
			System.out.print(i+" ");
		}
		System.out.println("");
		
		//while문
		int i = 1;
		while(i<=5) {
			System.out.print(i+" ");
			i++;
		}
		System.out.println(" ");
	
		//do while문
		i = 1;
		do {
			System.out.print(i+" ");
			i++;
		}while(i<=5);
		
		//-------------------------------------
		
		
		
		System.out.println("\n \n2번 문제");
		//for문
		for( i=5; i>=1; i--) {
			System.out.print(i+" ");
		}
		System.out.println("");
		
		//while문
		i = 5;
		while(i>=1) {
			System.out.print(i+" ");
			i--;
		}
		System.out.println(" ");
	
		//do while문
		i = 5;
		do {
			System.out.print(i+" ");
			i--;
		}while(i>=1);
		
		//-------------------------------------
		
		System.out.println("\n \n2번 문제");
		//for문
		for( i=1; i<=3; i++) {
			System.out.print("JAVA"+i+" ");
		}
		System.out.println("");
		
		//while문
		i = 1;
		while(i<=3) {
			System.out.print("JAVA"+i+" ");
			i++;
		}
		System.out.println(" ");
	
		//do while문
		i = 1;
		do {
			System.out.print("JAVA"+i+" ");
			i++;
		}while(i<=3);
		
		
		
		
		
		
	}//end psvm

}//end class
